package com.paperless.beans;

public class SpocDetails {
	
	private String spocName;
	private String appDesc;
	private String spocEmail;
	private String spocLocation;
	private String appPdl;
	private String mailTo;
	public String getSpocName() {
		return spocName;
	}
	public void setSpocName(String spocName) {
		this.spocName = spocName;
	}
	public String getAppDesc() {
		return appDesc;
	}
	public void setAppDesc(String appDesc) {
		this.appDesc = appDesc;
	}
	public String getSpocEmail() {
		return spocEmail;
	}
	public void setSpocEmail(String spocEmail) {
		this.spocEmail = spocEmail;
	}
	public String getSpocLocation() {
		return spocLocation;
	}
	public void setSpocLocation(String spocLocation) {
		this.spocLocation = spocLocation;
	}
	public String getAppPdl() {
		return appPdl;
	}
	public void setAppPdl(String appPdl) {
		this.appPdl = appPdl;
	}
	public String getMailTo() {
		return mailTo;
	}
	public void setMailTo(String mailTo) {
		this.mailTo = mailTo;
	}
	
	

}
